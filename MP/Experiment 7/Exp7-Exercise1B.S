  INCLUDE VICVPB.S
    AREA CHANG, CODE, READONLY
    EXPORT __main
__main
    ENTRY 
        LDR R1,=IO0DIR
        LDR R0,=0XFF0000
        STR R0,[R1]
        LDR R3,=IO0PIN
ADDER	STR R4,[R3]
		ADD R4,R4,#0X10000
		CMP R4,#0XFF0000
        BNE  ADDER
SUBBER	STR R4,[R3]
		SUB R4,R4,#0X10000
		CMP R4,#0X0000
        BNE  SUBBER
		BEQ ADDER
		END