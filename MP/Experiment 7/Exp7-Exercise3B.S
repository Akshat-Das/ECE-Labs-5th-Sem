PINSEL1 EQU 0XE002C004
DACR  EQU   0XE006C000
  AREA CHANG, CODE, READONLY
  EXPORT __main
__main
    ENTRY
        LDR R0,=PINSEL1
        LDR R1,=DACR
        LDR R2,=0X00080000
		LDR R5,=0XFFC0
        STR R2,[R0]
ADDER	STR R4,[R1]
		ADD R4,R4,#0X40
		CMP R4,R5
        BNE  ADDER
		BEQ SUBBER
SUBBER	STR R4,[R1]
		SUB R4,R4,#0X40
		CMP R4,#0X0000
        BNE  SUBBER
		BEQ ADDER
        END