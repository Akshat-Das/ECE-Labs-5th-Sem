IO0DIR	EQU 0XE0028008
IO0SET	EQU 0XE0028004
IO0CLR	EQU 0XE002800C
  AREA CHANG, CODE, READONLY
    EXPORT __main
__main
	ENTRY
		LDR	R1,=IO0DIR
		LDR	R0,=0XF000
		STR	R0,[R1]
		LDR	R2,=IO0CLR
		LDR	R3,=IO0SET
S		LDR R5,=0X8000
BACK	STR	R0,[R3]
		STR	R5,[R2]
		LDR	R4,=0X2FFF
delay	SUBS R4,R4,#1
		BNE	delay
		MOV	R5,R5,LSR #1
		CMP	R5,0X800
		BNE	BACK
		B	S
		END