IO0DIR	EQU 0XE0028008
IO0SET	EQU 0XE0028004
IO0CLR	EQU 0XE002800C
  AREA MOD16, CODE, READONLY
  EXPORT __main
__main
	ENTRY
		LDR R1,=IO0DIR
		LDR	R0,=0XF0000
		STR	R0,[R1]
		LDR	R2,=IO0CLR
		LDR	R3,=IO0SET
repeat	MOV	R4, #0XF0000
next	STR	R4,[R2]
		LDR	R5,=0X2FFFFF
delay	SUBS R5,R5,#1
		BNE	delay
		STR	R0,[R3]
		SUB R4,R4,#0X10000
		CMP	R4,#0X00000
		BNE next
repeat2 STR R4,[R2]
		LDR	R5,=0X2FFFFF
delay2	SUBS R5,R5,#1
		BNE	delay2
		B repeat
		END