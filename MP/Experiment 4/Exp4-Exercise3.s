IO0DIR	EQU 0XE0028008
IO0SET	EQU 0XE0028004
IO0CLR	EQU 0XE002800C
  AREA MOD16, CODE, READONLY
  EXPORT __main
__main
	ENTRY
		LDR R1,=IO0DIR
		LDR	R0,=0XFF0000
		STR	R0,[R1]
		LDR	R2,=IO0CLR
		LDR	R3,=IO0SET
		LDR R8,=0XF0000
		LDR R9,=0XF00000
repeat	LDR R11,=100
		LDR R4,=0XFF00FFFF
		LDR R10,=0XFF0000
next	STR	R10,[R2]
		LDR	R5,=0X2FFFFF
delay	SUBS R5,R5,#1
		BNE	delay
		STR	R0,[R3]
		ADD R4,R4,#0X10000
		MVN R10,R4
		AND R6,R4,R8
		CMP	R6,#0XA0000
		ADDGE R4,R4,#0X60000
		AND R7,R7,R4
		CMP R7,#0XA00000
		ADDGE R4,R4,#0X600000
		MVN R10,R4
		SUB R11,R11,#0X1
		CMP R11,#0X0
		BNE next
		B repeat
		END